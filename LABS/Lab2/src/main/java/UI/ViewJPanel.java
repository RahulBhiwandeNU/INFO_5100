    private void displayTableInfo() {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblVitals.getModel();
        defaultTableModel.setRowCount(0);
        vitalSignHistory.getHistory().stream().map(vsh -> {
            Object[] row = new Object[4];
            row[0] = vsh;
            row[1] = vsh.getTemperature();
            row[2] = vsh.getBloodPressure();
            row[3] = vsh.getPulse();
            return row;            
        }).forEachOrdered(row -> {
            defaultTableModel.addRow(row);
        });
    }
