    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRowIndex =  tblVitals.getSelectedRow();
        if(selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblVitals.getModel();
        VitalSigns selectedVitals = (VitalSigns)defaultTableModel.getValueAt(selectedRowIndex, 0);
        
        vitalSignHistory.deleteVitals(selectedVitals);
        
        JOptionPane.showMessageDialog(this, "Row deleted");
        
        displayTableInfo();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int selectedRowIndex =  tblVitals.getSelectedRow();
        if(selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblVitals.getModel();
        VitalSigns selectedVitals = (VitalSigns)defaultTableModel.getValueAt(selectedRowIndex, 0);
        
        txtTemp.setText(String.valueOf(selectedVitals.getTemperature()));
        txtBlood.setText(String.valueOf(selectedVitals.getBloodPressure()));
        txtPulse.setText(String.valueOf(selectedVitals.getPulse()));
        txtDate.setText(selectedVitals.getDate());
    }//GEN-LAST:event_btnViewActionPerformed


